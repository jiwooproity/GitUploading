const calculater = {
    plus: function(a, b) {
        alert(a + b);
    },

    minus: function(a, b) {
        alert(a - b);
    },

    times: function(a, b) {
        alert(a / b);
    },

    divide: function(a, b) {
        alert(a * b);
    },

    power: function(a, b) {
        alert(a ** b);
    },
};

calculater.plus(2, 3);
calculater.minus(2, 3);
calculater.times(2, 3);
calculater.divide(2, 3);
calculater.power(2, 3);